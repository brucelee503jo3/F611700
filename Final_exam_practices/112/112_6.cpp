#include<iostream>
using namespace std;
int main()
{
	int n;
	cin>>n;
	for(int i=0;i<n;i++)
		{
			//轉換成整數二微陣列 
			char ch[4][4];
			for(int j=0;j<4;j++)
				for(int k=0;k<4;k++)
					cin>>ch[j][k];
			int n[4][4];
			for(int j=0;j<4;j++)
				for(int k=0;k<4;k++)
					{
						if(ch[j][k]==35)
							n[j][k]=ch[j][k]-35;	
						else
							n[j][k]=ch[j][k]-48;							
					}
			//無限迴圈		
			while(!(n[0][0]!=0&&n[0][1]!=0&&n[0][2]!=0&&n[0][3]!=0
				&&n[1][0]!=0&&n[1][1]!=0&&n[1][2]!=0&&n[1][3]!=0
				&&n[2][0]!=0&&n[2][1]!=0&&n[2][2]!=0&&n[2][3]!=0
				&&n[3][0]!=0&&n[3][1]!=0&&n[3][2]!=0&&n[3][3]!=0))
				{
					int h=10;
					//第一列 
					if((h-n[0][0]-n[0][1]-n[0][2]-n[0][3])<5&&(h-n[0][0]-n[0][1]-n[0][2]-n[0][3])!=0)
						{
							h=h-n[0][0]-n[0][1]-n[0][2]-n[0][3];
							if(n[0][0]==0)n[0][0]=h;
							if(n[0][1]==0)n[0][1]=h;
							if(n[0][2]==0)n[0][2]=h;
							if(n[0][3]==0)n[0][3]=h;
							h=10;
						}
					//第二列	
					if((h-n[1][0]-n[1][1]-n[1][2]-n[1][3])<5&&(h-n[1][0]-n[1][1]-n[1][2]-n[1][3])!=0)
						{
							h=h-n[1][0]-n[1][1]-n[1][2]-n[1][3];
							if(n[1][0]==0)n[1][0]=h;
							if(n[1][1]==0)n[1][1]=h;
							if(n[1][2]==0)n[1][2]=h;
							if(n[1][3]==0)n[1][3]=h;
							h=10;
						}	
					//第三列	
					if((h-n[2][0]-n[2][1]-n[2][2]-n[2][3])<5&&(h-n[2][0]-n[2][1]-n[2][2]-n[2][3])!=0)
						{
							h=h-n[2][0]-n[2][1]-n[2][2]-n[2][3];
							if(n[2][0]==0)n[2][0]=h;
							if(n[2][1]==0)n[2][1]=h;
							if(n[2][2]==0)n[2][2]=h;
							if(n[2][3]==0)n[2][3]=h;
							h=10;
						}	
					//第四列	
					if((h-n[3][0]-n[3][1]-n[3][2]-n[3][3])<5&&(h-n[3][0]-n[3][1]-n[3][2]-n[3][3])!=0)
						{
							h=h-n[3][0]-n[3][1]-n[3][2]-n[3][3];
							if(n[3][0]==0)n[3][0]=h;
							if(n[3][1]==0)n[3][1]=h;
							if(n[3][2]==0)n[3][2]=h;
							if(n[3][3]==0)n[3][3]=h;
							h=10;
						}
					//第一行 
					if((h-n[0][0]-n[1][0]-n[2][0]-n[3][0])<5&&(h-n[0][0]-n[1][0]-n[2][0]-n[3][0])!=0)
						{
							h=h-n[0][0]-n[1][0]-n[2][0]-n[3][0];
							if(n[0][0]==0)n[0][0]=h;
							if(n[1][0]==0)n[1][0]=h;
							if(n[2][0]==0)n[2][0]=h;
							if(n[3][0]==0)n[3][0]=h;
							h=10;
						}
					//第二行 
					if((h-n[0][1]-n[1][1]-n[2][1]-n[3][1])<5&&(h-n[0][1]-n[1][1]-n[2][1]-n[3][1])!=0)
						{
							h=h-n[0][1]-n[1][1]-n[2][1]-n[3][1];
							if(n[0][1]==0)n[0][1]=h;
							if(n[1][1]==0)n[1][1]=h;
							if(n[2][1]==0)n[2][1]=h;
							if(n[3][1]==0)n[3][1]=h;
							h=10;
						}
					//第三行 
					if((h-n[0][2]-n[1][2]-n[2][2]-n[3][2])<5&&(h-n[0][2]-n[1][2]-n[2][2]-n[3][2])!=0)
						{
							h=h-n[0][2]-n[1][2]-n[2][2]-n[3][2];
							if(n[0][2]==0)n[0][2]=h;
							if(n[1][2]==0)n[1][2]=h;
							if(n[2][2]==0)n[2][2]=h;
							if(n[3][2]==0)n[3][2]=h;
							h=10;
						}
					//第四行 
					if((h-n[0][3]-n[1][3]-n[2][3]-n[3][3])<5&&(h-n[0][3]-n[1][3]-n[2][3]-n[3][3])!=0)
						{
							h=h-n[0][3]-n[1][3]-n[2][3]-n[3][3];
							if(n[0][3]==0)n[0][3]=h;
							if(n[1][3]==0)n[1][3]=h;
							if(n[2][3]==0)n[2][3]=h;
							if(n[3][3]==0)n[3][3]=h;
							h=10;
						}
					//第一區
					if((h-n[0][0]-n[0][1]-n[1][0]-n[1][1])<5&&(h-n[0][0]-n[0][1]-n[1][0]-n[1][1])!=0)
						{
							h=h-n[0][0]-n[0][1]-n[1][0]-n[1][1];
							if(n[0][0]==0)n[0][0]=h;
							if(n[0][1]==0)n[0][1]=h;
							if(n[1][0]==0)n[1][0]=h;
							if(n[1][1]==0)n[1][1]=h;
							h=10;
						}					
					//第二區
					if((h-n[0][2]-n[0][3]-n[1][2]-n[1][3])<5&&(h-n[0][2]-n[0][3]-n[1][2]-n[1][3])!=0)
						{
							h=h-n[0][2]-n[0][3]-n[1][2]-n[1][3];
							if(n[0][2]==0)n[0][2]=h;
							if(n[0][3]==0)n[0][3]=h;
							if(n[1][2]==0)n[1][2]=h;
							if(n[1][3]==0)n[1][3]=h;
							h=10;
						}
					//第三區
					if((h-n[2][0]-n[2][1]-n[3][0]-n[3][1])<5&&(h-n[2][0]-n[2][1]-n[3][0]-n[3][1])!=0)
						{
							h=h-n[2][0]-n[2][1]-n[3][0]-n[3][1];
							if(n[2][0]==0)n[2][0]=h;
							if(n[2][1]==0)n[2][1]=h;
							if(n[3][0]==0)n[3][0]=h;
							if(n[3][1]==0)n[3][1]=h;
							h=10;
						}
					//第四區
					if((h-n[2][2]-n[2][3]-n[3][2]-n[3][3])<5&&(h-n[2][2]-n[2][3]-n[3][2]-n[3][3])!=0)
						{
							h=h-n[2][2]-n[2][3]-n[3][2]-n[3][3];
							if(n[2][2]==0)n[2][2]=h;
							if(n[2][3]==0)n[2][3]=h;
							if(n[3][2]==0)n[3][2]=h;
							if(n[3][3]==0)n[3][3]=h;
							h=10;
						}																																																													
				}
			//輸出 
			for(int j=0;j<4;j++)
				{
					for(int k=0;k<4;k++)
						cout<<n[j][k];	
					cout<<endl;										
				}
		}	
	return 0;
}
